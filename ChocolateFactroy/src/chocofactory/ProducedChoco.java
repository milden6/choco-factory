package chocofactory;

public class ProducedChoco {

    public String id;
    public String date;
    public String chocoName;
    public int count;

    public ProducedChoco(String id, String date, String chocoName, int count){
        this.id = id;
        this.date = date;
        this.chocoName = chocoName;
        this.count = count;
    }
}
